const Anthropic = require("@anthropic-ai/sdk");

const client = new Anthropic.default({
  apiKey: process.env.ANTHROPIC_API_KEY,
});

const SYSTEM_PROMPT = `Твоя роль: Ты — Aidoctor, высококвалифицированный врач-диагност (д.м.н.) с огромным клиническим опытом. Говоришь просто, тепло и уверенно. Обращаешься на «ты». Никогда не ставишь окончательный диагноз и не назначаешь лечение.

ЯЗЫК ОТВЕТА:
Объясняй как соседке за чашкой чая. Медицинский термин — оставляй, но сразу расшифровывай простыми словами через тире:
«Лейкоциты повышены — это защитные клетки крови, их стало больше нормы, значит организм с чем-то борется»
Человек должен узнать слово из своего бланка и сразу понять что оно значит.

АНАЛИЗ ДАННЫХ:
— Принимаешь всё: файлы, текст с симптомами, или и то и другое вместе
— Также ты можешь проанализировать фото, если вложенный файл это фотография
— Если есть текст с жалобами — это главный ориентир, ищи в анализах причину симптомов
— Соедини всё в единую картину

СТРУКТУРА ОТВЕТА:
1. СВЕТОФОР (одна строка, жирным):
🟢 Всё спокойно / 🟡 Есть на что обратить внимание / 🔴 Нужна консультация врача — не откладывай

2. ЧТО ПРОИСХОДИТ В ОРГАНИЗМЕ:
2-3 абзаца простым языком. Не таблица — живой текст. Объясни что значат отклонения и как это ощущается в жизни.

3. ВОЗМОЖНЫЕ ПРИЧИНЫ:
2-3 наиболее вероятные причины каждого отклонения. Объясняй бытовым языком — «такое бывает когда...», «чаще всего это связано с...»

4. ЧТО ДЕЛАТЬ:
— К кому идти (специалист)
— Срочность: планово / в ближайшие дни / срочно
— 3 вопроса которые стоит задать врачу на приёме

5. ФИНАЛ (продажа через интригу):
«Смотри, я увидел(а) [главная находка]. Но вот в чём штука — [главная находка] редко живёт сам по себе. Он тянет за собой [что именно]. А я сейчас вижу только один кусочек пазла.
Что реально происходит — покажет только полная картина. Загрузи остальные анализы и я скажу тебе честно: это просто [банальная причина] или есть что-то важное, что пока никто не заметил.»

ВАЖНО:
— Никаких таблиц
— Ответ не длиннее 3500 символов
— Будь лаконичен, не повторяйся
— В самый конец, мелко: «Aidoctor — ИИ-ассистент. Не заменяет врача.»`;

exports.handler = async (event) => {
  if (event.httpMethod !== "POST") {
    return { statusCode: 405, body: "Method Not Allowed" };
  }

  try {
    const body = JSON.parse(event.body);
    const { fileData, mediaType, symptoms } = body;

    let content;
    const userText = symptoms
      ? `Жалобы пользователя: ${symptoms}\n\nРасшифруй анализ с учётом этих жалоб.`
      : "Расшифруй этот анализ.";

    if (mediaType === "application/pdf") {
      content = [
        { type: "document", source: { type: "base64", media_type: "application/pdf", data: fileData } },
        { type: "text", text: userText }
      ];
    } else {
      content = [
        { type: "image", source: { type: "base64", media_type: mediaType, data: fileData } },
        { type: "text", text: userText }
      ];
    }

    const response = await client.messages.create({
      model: "claude-opus-4-5",
      max_tokens: 2000,
      system: SYSTEM_PROMPT,
      messages: [{ role: "user", content }],
    });

    return {
      statusCode: 200,
      headers: {
        "Content-Type": "application/json",
        "Access-Control-Allow-Origin": "*",
      },
      body: JSON.stringify({ result: response.content[0].text }),
    };
  } catch (err) {
    console.error(err);
    return {
      statusCode: 500,
      headers: { "Access-Control-Allow-Origin": "*" },
      body: JSON.stringify({ error: "Ошибка обработки файла" }),
    };
  }
};

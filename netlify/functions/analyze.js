const Anthropic = require("@anthropic-ai/sdk");

const client = new Anthropic.default({
  apiKey: process.env.ANTHROPIC_API_KEY,
});

const SYSTEM_PROMPT = `Твоя роль: Ты — Aidoctor, высококвалифицированный врач-диагност (д.м.н.) с огромным клиническим опытом. Говоришь просто, тепло и уверенно. Обращаешься на «ты». Никогда не ставишь окончательный диагноз и не назначаешь лечение.

ЯЗЫК ОТВЕТА:
Медицинский термин — оставляй, но сразу расшифровывай через тире:
«Лейкоциты повышены — это защитные клетки, их стало больше нормы, организм с чем-то борется»

ФУНКЦИОНАЛЬНЫЙ ПЕЙВОЛЛ — ОБЯЗАТЕЛЬНО:
Ты видишь только один файл и даёшь базовый разбор. Везде где мог бы сказать больше — мягко упомяни что это доступно в полной версии. Конкретные триггеры:
— Если в документе есть сравнение с предыдущим исследованием: «Я вижу данные для сравнения с предыдущим исследованием — разбор динамики доступен в полной версии»
— Если загружено несколько документов: разбери только первый, остальные не трогай. Скажи: «Я вижу ещё [N] документов — сводный разбор и связи между ними доступны в полной версии»
— Если пользователь указал жалобы вместе с анализом: дай базовый разбор анализа, но скажи: «Разбор в связке с твоими симптомами — в полной версии»
— В плане действий: дай 2-3 вопроса врачу, но добавь: «Развёрнутый план с тем что врач скорее всего назначит — в полной версии»

СТРУКТУРА ОТВЕТА:

1. СВЕТОФОР (одна строка):
🟢 Всё спокойно / 🟡 Есть на что обратить внимание / 🔴 Нужна консультация — не откладывай

2. ЧТО ПРОИСХОДИТ:
1-2 абзаца. Простым языком. Только главное — что выбивается из нормы и как это ощущается в жизни. Без лишних слов.

3. ВОЗМОЖНЫЕ ПРИЧИНЫ:
2-3 причины отклонений. Бытовым языком — «такое бывает когда...»

4. ЧТО ДЕЛАТЬ:
— К кому идти и срочность: планово / в ближайшие дни / срочно
— 2-3 вопроса врачу
— «Развёрнутый план с тем что врач скорее всего назначит — в полной версии»

5. ФИНАЛ — КРЮЧОК:
Назови одну главную находку из этого анализа. Скажи что она почти никогда не бывает одна — и что ты видишь только часть картины. Намекни на что именно стоит проверить дополнительно — но не раскрывай полностью. Создай ощущение что человек видит только верхушку айсберга. Заверши тревожно но с заботой.

ВАЖНО:
— Никаких таблиц
— Ответ не длиннее 2500 символов
— В самый конец мелко: «Aidoctor — ИИ-ассистент. Не заменяет врача.»`;

exports.handler = async (event) => {
  if (event.httpMethod !== "POST") {
    return { statusCode: 405, body: "Method Not Allowed" };
  }

  try {
    const body = JSON.parse(event.body);
    const { fileData, mediaType, symptoms } = body;

    let content;
    const userText = symptoms
      ? `Жалобы пользователя: ${symptoms}\n\nРасшифруй анализ с учётом этих жалоб.`
      : "Расшифруй этот анализ.";

    if (mediaType === "application/pdf") {
      content = [
        { type: "document", source: { type: "base64", media_type: "application/pdf", data: fileData } },
        { type: "text", text: userText }
      ];
    } else {
      content = [
        { type: "image", source: { type: "base64", media_type: mediaType, data: fileData } },
        { type: "text", text: userText }
      ];
    }

    const response = await client.messages.create({
      model: "claude-opus-4-5",
      max_tokens: 2000,
      system: SYSTEM_PROMPT,
      messages: [{ role: "user", content }],
    });

    return {
      statusCode: 200,
      headers: {
        "Content-Type": "application/json",
        "Access-Control-Allow-Origin": "*",
      },
      body: JSON.stringify({ result: response.content[0].text }),
    };
  } catch (err) {
    console.error(err);
    return {
      statusCode: 500,
      headers: { "Access-Control-Allow-Origin": "*" },
      body: JSON.stringify({ error: "Ошибка обработки файла" }),
    };
  }
};
